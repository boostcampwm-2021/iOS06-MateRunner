import UIKit

extension UIColor {
    static var mrPurple: UIColor {
        return UIColor(red: 122.0 / 255.0, green: 126.0 / 255.0, blue: 247.0 / 255.0, alpha: 1)
    }

    static var mrYellow: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 233.0 / 255.0, blue: 129.0 / 255.0, alpha: 1)
    }
    
    static var mrGray: UIColor {
        return UIColor(red: 200.0 / 255.0, green: 200.0 / 255.0, blue: 200.0 / 255.0, alpha: 1)
    }
}
